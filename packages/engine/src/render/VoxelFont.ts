export const FONT_W = 5;
export const FONT_H = 7;
export const FONT_GAP = 1;

/** Map common Unicode punctuation to ASCII glyphs the font actually has. */
const NORMALIZE: Record<string, string> = {
  '\u2018': "'", '\u2019': "'", '\u201A': ',', '\u201B': "'",
  '\u201C': '"', '\u201D': '"', '\u201E': '"',
  '\u2013': '-', '\u2014': '-', '\u2212': '-',
  '\u2026': '.', '\u00A0': ' ',
};

export function normalizeText(text: string): string {
  let out = '';
  for (const ch of text) out += NORMALIZE[ch] ?? ch;
  return out;
}

export function glyphRows(ch: string): number[] {
  return GLYPHS[ch] ?? GLYPHS[NORMALIZE[ch] ?? ''] ?? GLYPHS['?']!;
}

export function textWidth(text: string, scale = 1): number {
  if (!text) return 0;
  const cell = (FONT_W + FONT_GAP) * scale;
  return text.length * cell - FONT_GAP * scale;
}

export type TextStamp = (x: number, y: number) => void;

/** Stamp each lit pixel of `text` via callback (canvas coordinates). */
export function stampText(text: string, x: number, y: number, scale: number, stamp: TextStamp): void {
  let cx = x;
  for (const ch of text) {
    const rows = glyphRows(ch);
    for (let row = 0; row < FONT_H; row++) {
      const bits = rows[row] ?? 0;
      for (let col = 0; col < FONT_W; col++) {
        if (bits & (0x10 >> col)) {
          for (let sy = 0; sy < scale; sy++) {
            for (let sx = 0; sx < scale; sx++) {
              stamp(cx + col * scale + sx, y + row * scale + sy);
            }
          }
        }
      }
    }
    cx += (FONT_W + FONT_GAP) * scale;
  }
}

/** Wrap text to fit max pixel width; returns lines. */
export function wrapText(text: string, maxWidth: number, scale = 1): string[] {
  const words = text.split(/\s+/);
  const lines: string[] = [];
  let line = '';
  for (const w of words) {
    const test = line ? `${line} ${w}` : w;
    if (textWidth(test, scale) <= maxWidth) line = test;
    else {
      if (line) lines.push(line);
      line = w;
    }
  }
  if (line) lines.push(line);
  return lines.length ? lines : [''];
}
